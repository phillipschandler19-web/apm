class TestCaseBScopedInstructions:
    """Case B: scoped instructions already in .github/instructions/ -> NO subdir AGENTS.md."""

    @pytest.fixture
    def project_scoped(self, tmp_path: Path):
        """Project with scoped instructions (docs/**/*.md, src/**/*.py) deployed to .github/."""
        # Create source files
        (tmp_path / "docs").mkdir()
        (tmp_path / "docs" / "readme.md").write_text("# doc", encoding="utf-8")
        (tmp_path / "src").mkdir()
        (tmp_path / "src" / "main.py").write_text("x = 1", encoding="utf-8")

        instr_dir = tmp_path / ".apm" / "instructions"
        instr_dir.mkdir(parents=True)

        docs_file = instr_dir / "docs.instructions.md"
        docs_file.write_text(
            "---\ndescription: Markdown docs\napplyTo: 'docs/**/*.md'\n---\n"
            "Docs writing guidance.\n",
            encoding="utf-8",
        )
        src_file = instr_dir / "src.instructions.md"
        src_file.write_text(
            "---\ndescription: Python source\napplyTo: 'src/**/*.py'\n---\n"
            "Python coding guidance.\n",
            encoding="utf-8",
        )

        # Deploy to .github/instructions/
        gh_instr_dir = tmp_path / ".github" / "instructions"
        gh_instr_dir.mkdir(parents=True)
        (gh_instr_dir / "docs.instructions.md").write_text(
            docs_file.read_text(encoding="utf-8"), encoding="utf-8"
        )
        (gh_instr_dir / "src.instructions.md").write_text(
            src_file.read_text(encoding="utf-8"), encoding="utf-8"
        )

        docs_inst = _make_instruction(
            name="docs",
            content="Docs writing guidance.",
            apply_to="docs/**/*.md",
            file_path=docs_file,
        )
        src_inst = _make_instruction(
            name="src",
            content="Python coding guidance.",
            apply_to="src/**/*.py",
            file_path=src_file,
        )
        primitives = _make_primitives(docs_inst, src_inst)
        return tmp_path, primitives

    def test_case_b_no_subdir_agents_md_written(self, project_scoped):
        """Failing-without-fix: no docs/AGENTS.md or src/AGENTS.md when dedup fires."""
        tmp_path, primitives = project_scoped
        compiler = AgentsCompiler(str(tmp_path))
        config = CompilationConfig(target="vscode", dry_run=False)

        compiler._compile_distributed(config, primitives)

        docs_agents_md = tmp_path / "docs" / "AGENTS.md"
        src_agents_md = tmp_path / "src" / "AGENTS.md"

        assert not docs_agents_md.exists(), (
            "docs/AGENTS.md must NOT be created when instructions are already in"
            " .github/instructions/ and the body would be empty."
        )
        assert not src_agents_md.exists(), (
            "src/AGENTS.md must NOT be created when instructions are already in"
            " .github/instructions/ and the body would be empty."
        )

    def test_case_b_multiple_suppressed_paths(self, project_scoped):
        """suppressed_empty_paths contains both docs and src entries."""
        from apm_cli.compilation.distributed_compiler import DistributedAgentsCompiler

        tmp_path, primitives = project_scoped
        dist_compiler = DistributedAgentsCompiler(str(tmp_path))
        config = {
            "skip_instructions": True,
            "source_attribution": False,
            "clean_orphaned": False,
            "dry_run": False,
            "debug": False,
        }
        result = dist_compiler.compile_distributed(primitives, config=config)
        assert result.success
        # At least the subdir placements (docs/AGENTS.md, src/AGENTS.md) should
        # be suppressed; root may or may not be in the placement depending on
        # context optimizer decisions.
        assert len(result.suppressed_empty_paths) >= 1


