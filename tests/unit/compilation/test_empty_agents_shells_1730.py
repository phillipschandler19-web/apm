"""Acceptance tests for issue #1730: apm compile writes empty AGENTS.md shells.

All tests in this module correspond to the acceptance criteria from the issue:

  Case A -- root AGENTS.md (global instructions)
  Case B -- distributed subdir AGENTS.md (scoped instructions)
  --clean removes pre-existing APM-generated empty shells
  Hand-authored AGENTS.md (no marker) never deleted by --clean
  Plain `apm compile` stays non-destructive (suppresses new writes only)
  Multi-target (Codex/OpenCode/Windsurf/Gemini) unaffected
  --no-dedup / --force-instructions still produces full AGENTS.md
  INFO-style "not generated" message emitted

Mutation check: tests are written so that removing the empty-shell guard in
distributed_compiler.py or agents_compiler.py causes them to fail.
"""

from __future__ import annotations

from pathlib import Path

import pytest

from apm_cli.compilation.agents_compiler import AgentsCompiler, CompilationConfig
from apm_cli.compilation.distributed_compiler import (
    AGENTS_MD_GENERATED_MARKER,
    DistributedAgentsCompiler,
    PlacementResult,
)
from apm_cli.primitives.models import Instruction, PrimitiveCollection

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _make_instruction(
    name: str,
    content: str = "Follow these rules.",
    apply_to: str | None = None,
    file_path: Path | None = None,
    tmp_path: Path | None = None,
) -> Instruction:
    if file_path is None:
        # Derive a tmp_path-scoped path so each test invocation gets a unique,
        # isolated location -- no shared temp-dir collisions across xdist workers
        # and no stale files between runs.
        if tmp_path is None:
            raise ValueError("Either file_path or tmp_path must be provided to _make_instruction")
        file_path = tmp_path / f"{name}.instructions.md"
    return Instruction(
        name=name,
        file_path=file_path,
        description=f"{name} instruction",
        apply_to=apply_to,
        content=content,
        author="test",
        source="local",
    )


def _make_primitives(*instructions: Instruction) -> PrimitiveCollection:
    col = PrimitiveCollection()
    for inst in instructions:
        col.add_primitive(inst)
    return col


def _apm_generated_marker_content(extra: str = "") -> str:
    """Produce minimal APM-generated AGENTS.md content (marker present)."""
    return (
        f"# AGENTS.md\n"
        f"{AGENTS_MD_GENERATED_MARKER}\n"
        f"<!-- Build ID: abc123def456 -->\n"
        f"<!-- APM Version: 0.0.0 -->\n"
        f"\n"
        f"---\n"
        f"*This file was generated by APM CLI. Do not edit manually.*\n"
        f"*To regenerate: `apm compile`*\n"
        f"{extra}\n"
    )


