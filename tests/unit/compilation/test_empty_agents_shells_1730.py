class TestCleanRemovesEmptyShells:
    """apm compile --clean removes pre-existing APM-generated empty AGENTS.md shells."""

    @pytest.fixture
    def project_with_stale_shell(self, tmp_path: Path):
        """Project where a previous run left an empty-shell AGENTS.md."""
