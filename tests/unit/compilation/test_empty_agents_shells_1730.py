class TestCaseAGlobalInstructions:
    """Case A: copilot target (internal alias `vscode`), .github/instructions/*.md present -> NO root AGENTS.md."""

    @pytest.fixture
    def project_global(self, tmp_path: Path):
        """Project with a global (no applyTo) instruction, .github/instructions/ populated."""
        # Write instruction primitive
        instr_dir = tmp_path / ".apm" / "instructions"
        instr_dir.mkdir(parents=True)
        instr_file = instr_dir / "global.instructions.md"
        instr_file.write_text(
            "---\ndescription: A global instruction\n---\n"
            "This is global guidance that should reach Copilot.\n",
            encoding="utf-8",
        )

        # Deploy to .github/instructions/ (simulates `apm install`)
        gh_instr_dir = tmp_path / ".github" / "instructions"
        gh_instr_dir.mkdir(parents=True)
        (gh_instr_dir / "global.instructions.md").write_text(
            "---\ndescription: A global instruction\n---\n"
            "This is global guidance that should reach Copilot.\n",
            encoding="utf-8",
        )

        instruction = _make_instruction(
            name="global",
            content="This is global guidance that should reach Copilot.",
            apply_to=None,
            file_path=instr_file,
        )
        primitives = _make_primitives(instruction)
        return tmp_path, primitives

    def test_case_a_no_root_agents_md_written(self, project_global):
        """Failing-without-fix: copilot target (internal alias `vscode`) + .github/instructions/ -> NO root AGENTS.md."""
        tmp_path, primitives = project_global
        compiler = AgentsCompiler(str(tmp_path))
        config = CompilationConfig(target="vscode", dry_run=False)

        compiler._compile_distributed(config, primitives)

        agents_md = tmp_path / "AGENTS.md"
        assert not agents_md.exists(), (
            "AGENTS.md must NOT be created when instructions are already in"
            " .github/instructions/ and the body would be empty.\n"
            f"Content written:\n{agents_md.read_text() if agents_md.exists() else '(none)'}"
        )

    def test_case_a_suppressed_path_in_result(self, project_global):
        """suppressed_empty_paths is populated in the distributed result."""
        from apm_cli.compilation.distributed_compiler import DistributedAgentsCompiler

        tmp_path, primitives = project_global
        dist_compiler = DistributedAgentsCompiler(str(tmp_path))
        config = {
            "skip_instructions": True,
            "source_attribution": False,
            "clean_orphaned": False,
            "dry_run": False,
            "debug": False,
        }
        result = dist_compiler.compile_distributed(primitives, config=config)
        assert result.success
        assert len(result.suppressed_empty_paths) > 0, (
            "suppressed_empty_paths must be non-empty when dedup fires and placement"
            " would be an empty shell"
        )

    def test_case_a_no_dedup_still_writes_agents_md(self, project_global):
        """--no-dedup must always produce AGENTS.md even with .github/instructions/ present."""
        tmp_path, primitives = project_global
        compiler = AgentsCompiler(str(tmp_path))
        config = CompilationConfig(target="vscode", no_dedup=True, dry_run=False)

        compiler._compile_distributed(config, primitives)

        agents_md = tmp_path / "AGENTS.md"
        assert agents_md.exists(), (
            "AGENTS.md must be written when --no-dedup is set, regardless of"
            " .github/instructions/ state."
        )
        body = agents_md.read_text(encoding="utf-8")
        assert "global guidance" in body, (
            "--no-dedup must include instruction content in AGENTS.md. Got:\n" + body
        )


