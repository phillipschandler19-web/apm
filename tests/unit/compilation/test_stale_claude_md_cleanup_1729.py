"""Regression tests for issue #1729 -- stale CLAUDE.md never removed after
instructions move to .claude/rules/.

Acceptance criteria:
- Existing APM-generated CLAUDE.md + populated .claude/rules/ + apm compile --clean
  -> CLAUDE.md removed.
- Hand-authored CLAUDE.md (no marker) is NEVER removed; warning emitted instead.
- Plain apm compile (no --clean) still deletes nothing (non-destructive).
- Clean-checkout behavior from #1138 unchanged (no CLAUDE.md ever created when
  .claude/rules/ already populated).
- --no-dedup / --force-instructions still regenerate a full CLAUDE.md and never
  trigger removal.
"""

from __future__ import annotations

from pathlib import Path

import pytest

from apm_cli.compilation.agents_compiler import AgentsCompiler, CompilationConfig
from apm_cli.compilation.claude_formatter import CLAUDE_HEADER
from apm_cli.compilation.constitution import clear_constitution_cache
from apm_cli.primitives.models import Instruction, PrimitiveCollection
from apm_cli.utils.path_security import PathTraversalError

