class TestCleanRemovesApmGeneratedStaleCLAUDEMD:
    """apm compile --clean must remove a stale APM-generated CLAUDE.md when
    instructions are already in .claude/rules/ (issue #1729 primary fix)."""

    def test_clean_removes_apm_generated_claude_md(self, tmp_path):
        """--clean + .claude/rules/ populated + APM-generated CLAUDE.md -> file removed."""
        _make_project(tmp_path, populate_rules=True)
        _apm_generated_claude_md(tmp_path)

        compiler = AgentsCompiler(str(tmp_path))
        config = CompilationConfig(
            target="claude",
            clean_orphaned=True,
            dry_run=False,
        )
        primitives = _make_primitives(tmp_path)
        result = compiler._compile_claude_md(config, primitives)

        assert result.success, f"Expected success, got errors: {result.errors}"
        assert not (tmp_path / "CLAUDE.md").exists(), (
            "APM-generated CLAUDE.md must be removed by --clean when .claude/rules/ is populated"
        )

    def test_clean_log_mentions_removal(self, tmp_path):
        """A success-level log about the removal must be emitted (green/bold)."""
        _make_project(tmp_path, populate_rules=True)
        _apm_generated_claude_md(tmp_path)

        logger = _CapturingLogger()
        compiler = AgentsCompiler(str(tmp_path))
        compiler._logger = logger
        config = CompilationConfig(
            target="claude",
            clean_orphaned=True,
            dry_run=False,
        )
        primitives = _make_primitives(tmp_path)
        compiler._compile_claude_md(config, primitives)

        # Must be logged at "success" level (green) -- a destructive file deletion
        # must render visibly distinct from an info/progress no-op (issue #1729 fix).
        success_messages = [m for level, m in logger.logged if level == "success"]
        assert any("Removed stale" in m and "CLAUDE.md" in m for m in success_messages), (
            f"Expected a 'Removed stale CLAUDE.md' log line at 'success' level, got: {logger.logged}"
        )

    def test_clean_checks_containment_before_unlink(self, tmp_path, monkeypatch):
        """Stale CLAUDE.md deletion must pass through the path containment guard."""
        _make_project(tmp_path, populate_rules=True)
        _apm_generated_claude_md(tmp_path)
        guard_calls = []

        def fake_ensure_path_within(path: Path, base_dir: Path) -> Path:
            guard_calls.append((path, base_dir))
            return path

        monkeypatch.setattr(
            "apm_cli.compilation.agents_compiler.ensure_path_within",
            fake_ensure_path_within,
        )

        compiler = AgentsCompiler(str(tmp_path))
        config = CompilationConfig(
            target="claude",
            clean_orphaned=True,
            dry_run=False,
        )
        primitives = _make_primitives(tmp_path)
        compiler._compile_claude_md(config, primitives)

        assert guard_calls == [
            (tmp_path / "CLAUDE.md", tmp_path),
            (tmp_path / "CLAUDE.md", tmp_path),
        ]
        assert not (tmp_path / "CLAUDE.md").exists()

    def test_clean_keeps_file_when_containment_guard_rejects(self, tmp_path, monkeypatch):
        """Containment failure must fail closed before reading or unlinking."""
        _make_project(tmp_path, populate_rules=True)
        _apm_generated_claude_md(tmp_path)

        def reject_path(path: Path, base_dir: Path) -> Path:
            raise PathTraversalError("outside base")

        monkeypatch.setattr("apm_cli.compilation.agents_compiler.ensure_path_within", reject_path)
        logger = _CapturingLogger()
        compiler = AgentsCompiler(str(tmp_path))
        compiler._logger = logger
        config = CompilationConfig(
            target="claude",
            clean_orphaned=True,
            dry_run=False,
        )
        primitives = _make_primitives(tmp_path)
        result = compiler._compile_claude_md(config, primitives)

        assert (tmp_path / "CLAUDE.md").exists()
        assert any("Could not read CLAUDE.md" in w for w in result.warnings)
        assert any(
            "Could not read CLAUDE.md" in m for level, m in logger.logged if level == "warning"
        )


