class _CapturingLogger:
    """Minimal logger stub that records (level, message) pairs for assertions."""

    def __init__(self):
        self.logged: list[tuple[str, str]] = []

    def progress(self, msg, **kw):
        self.logged.append(("progress", msg))

    def success(self, msg, **kw):
        self.logged.append(("success", msg))

    def warning(self, msg, **kw):
        self.logged.append(("warning", msg))

    def verbose_detail(self, msg, **kw):
        pass


def _make_project(tmp_path, *, populate_rules: bool = True):
    """Create a minimal project with an instruction primitive.

    If populate_rules=True a .claude/rules/*.md file is created so that
    _detect_deployed_instructions returns True (skip_instructions fires).
    """
    (tmp_path / "apm.yml").write_text(
        "name: test-stale-claude\nversion: 1.0.0\ntargets:\n  - claude\n",
        encoding="utf-8",
    )
    instr_dir = tmp_path / ".apm" / "instructions"
    instr_dir.mkdir(parents=True)
    (instr_dir / "global.instructions.md").write_text(
        "---\ndescription: A global instruction\n---\nThis is global guidance.\n",
        encoding="utf-8",
    )
    if populate_rules:
        rules_dir = tmp_path / ".claude" / "rules"
        rules_dir.mkdir(parents=True)
        (rules_dir / "global.md").write_text("This is global guidance.\n", encoding="utf-8")


def _apm_generated_claude_md(tmp_path) -> None:
    """Write a CLAUDE.md that looks like one generated by APM (has CLAUDE_HEADER)."""
    (tmp_path / "CLAUDE.md").write_text(
        f"{CLAUDE_HEADER}\n\n# Project Standards\n\nThis is global guidance.\n",
        encoding="utf-8",
    )


def _hand_authored_claude_md(tmp_path) -> None:
    """Write a CLAUDE.md without the APM marker (hand-authored)."""
    (tmp_path / "CLAUDE.md").write_text(
        "# My Custom Instructions\n\nHand-written content.\n",
        encoding="utf-8",
    )


def _make_primitives(tmp_path) -> PrimitiveCollection:
    primitives = PrimitiveCollection()
    primitives.add_primitive(
        Instruction(
            name="global",
            file_path=tmp_path / ".apm/instructions/global.instructions.md",
            description="A global instruction",
            apply_to="",
            content="This is global guidance.",
            author="test",
            source="local",
        )
    )
    return primitives


